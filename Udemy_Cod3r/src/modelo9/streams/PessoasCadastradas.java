package streams;

public class PessoasCadastradas {
	
	final String nome;
	final String nomeDoMeio;
	final String sobrenome;
	final int idade;
	
	public PessoasCadastradas(String nome, String nomeDoMeio, String sobrenome, int idade) {
		super();
		this.nome = nome;
		this.nomeDoMeio = nomeDoMeio;
		this.sobrenome = sobrenome;
		this.idade = idade;
	}
	
	

}
